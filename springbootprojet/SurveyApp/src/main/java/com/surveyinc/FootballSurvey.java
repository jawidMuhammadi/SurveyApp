package com.surveyinc;

public class FootballSurvey {

	private String name;
	private String lastName;
	private String sext;
	private String birthdate;
	private String footballTeam;
	private String notes;
	private String surveyerName;
	private String surveyerLastName;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getSext() {
		return sext;
	}
	public void setSext(String sext) {
		this.sext = sext;
	}
	public String getBirthdate() {
		return birthdate;
	}
	public void setBirthdate(String birthdate) {
		this.birthdate = birthdate;
	}
	public String getFootballTeam() {
		return footballTeam;
	}
	public void setFootballTeam(String footballTeam) {
		this.footballTeam = footballTeam;
	}
	public String getNotes() {
		return notes;
	}
	public void setNotes(String notes) {
		this.notes = notes;
	}
	public String getSurveyerName() {
		return surveyerName;
	}
	public void setSurveyerName(String surveyerName) {
		this.surveyerName = surveyerName;
	}
	public String getSurveyerLastName() {
		return surveyerLastName;
	}
	public void setSurveyerLastName(String surveyerLastName) {
		this.surveyerLastName = surveyerLastName;
	}

	
}
